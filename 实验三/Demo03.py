# 定义一个字典，用于将阿拉伯数字映射到对应的中文大写数字字符
chinese_nums = {'0': '零', '1': '壹', '2': '贰', '3': '叁',
                '4': '肆',  '5':  "伍",  "6": "陆",
                "7":"柒","8":"捌","9":"玖"}
# 获取用户输入的整数并转换为字符串形式
num = str(input("请输入需要转换为中文大写数字的整数："))
# 将每个阿拉伯数字替换为对应的中文大写数字字符，并拼接成最终结果字符串
result = ''
for digit in num:
    result += chinese_nums[digit]
print(result)